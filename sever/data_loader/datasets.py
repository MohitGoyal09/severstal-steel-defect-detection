class SyntheticDefectDataset(Dataset):
    """
    Dataset that reads synthetic defect images and masks generated by the GAN.
    """

    def __init__(
        self, synthetic_root, metadata_csv="metadata_filtered.csv", transforms=None
    ):
        self.root = Path(synthetic_root)
        self.img_dir = self.root / "images"
        self.mask_dir = self.root / "masks"
        self.transforms = transforms

        meta_path = self.root / metadata_csv
        if not meta_path.exists():
            self.samples = []
            for cls_dir in sorted(self.img_dir.glob("cls_*")):
                cls_id = int(cls_dir.name.split("_")[1]) - 1
                for img_path in sorted(cls_dir.glob("*.png")):
                    self.samples.append(
                        {
                            "filename": f"{cls_dir.name}/{img_path.name}",
                            "class_id": cls_id,
                        }
                    )
        else:
            df = pd.read_csv(meta_path)
            self.samples = df.to_dict("records")
            for s in self.samples:
                s["class_id"] = int(s.get("class_id", 1)) - 1

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        sample = self.samples[idx]
        fname = sample["filename"]

        img_path = self.img_dir / fname
        mask_path = self.mask_dir / fname

        img = cv2.imread(str(img_path), cv2.IMREAD_GRAYSCALE)
        if img is None:
            img = np.zeros((256, 256), dtype=np.uint8)
        img = img.astype(np.float32) / 255.0

        if mask_path.exists():
            mask = cv2.imread(str(mask_path), cv2.IMREAD_GRAYSCALE)
            mask = (mask > 127).astype(np.float32)
        else:
            mask = np.zeros_like(img)

        img = img[:, :, np.newaxis]
        mask = mask[:, :, np.newaxis]

        if self.transforms is not None:
            augmented = self.transforms(image=img, mask=mask)
            img = augmented["image"]
            mask = augmented["mask"]

        if not isinstance(img, torch.Tensor):
            img = torch.from_numpy(img).permute(2, 0, 1).float()
            mask = torch.from_numpy(mask).permute(2, 0, 1).float()

        return img, mask


class MixedSeverstalDataset(Dataset):
    """
    Mixes real and synthetic defect samples with a configurable ratio.
    """

    def __init__(self, real_ds, synth_ds, synth_ratio=0.3):
        self.real_ds = real_ds
        self.synth_ds = synth_ds
        self.synth_ratio = synth_ratio

    def set_synth_ratio(self, ratio):
        self.synth_ratio = ratio

    def __len__(self):
        return len(self.real_ds)

    def __getitem__(self, idx):
        if random.random() < self.synth_ratio and len(self.synth_ds) > 0:
            return self.synth_ds[random.randint(0, len(self.synth_ds) - 1)]
        return self.real_ds[idx]
